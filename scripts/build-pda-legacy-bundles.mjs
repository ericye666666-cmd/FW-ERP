import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { transform } from "esbuild";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const bundles = [
  {
    source: "frontend_prototype/app.js",
    output: "frontend_prototype/app.legacy.js",
  },
  {
    source: "frontend_prototype/operations-fulfillment-flow.js",
    output: "frontend_prototype/operations-fulfillment-flow.legacy.js",
  },
];
const legacyPolyfills = `
(function installPdaLegacyPolyfills() {
  if (!String.prototype.replaceAll) {
    Object.defineProperty(String.prototype, "replaceAll", {
      configurable: true,
      writable: true,
      value: function replaceAll(searchValue, replaceValue) {
        var target = String(this);
        if (searchValue instanceof RegExp) {
          if (!searchValue.global) {
            throw new TypeError("String.prototype.replaceAll called with a non-global RegExp argument");
          }
          return target.replace(searchValue, replaceValue);
        }
        var searchString = String(searchValue);
        if (searchString === "") {
          return target.replace(/(?:)/g, String(replaceValue));
        }
        return target.split(searchString).join(String(replaceValue));
      },
    });
  }
})();
`;

for (const bundle of bundles) {
  const sourcePath = path.join(repoRoot, bundle.source);
  const outputPath = path.join(repoRoot, bundle.output);
  const source = await readFile(sourcePath, "utf8");
  const result = await transform(source, {
    charset: "utf8",
    legalComments: "none",
    loader: "js",
    logLevel: "silent",
    sourcemap: false,
    target: ["chrome74"],
  });
  const banner = [
    "// Generated by scripts/build-pda-legacy-bundles.mjs. Do not edit directly.",
    `// Source: ${bundle.source}`,
    "",
  ].join("\n");
  await writeFile(outputPath, banner + legacyPolyfills + "\n" + result.code.replace(/\r\n/g, "\n"), "utf8");
  console.log(`${bundle.output} <- ${bundle.source}`);
}
